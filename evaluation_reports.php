<script>
	$(document).ready(function(){
		$('#list').dataTable();
		$('.delete_evaluation_report').click(function(){
			_conf("Are you sure to delete this evaluation report?","delete_evaluation_report",[$(this).attr('data-id')])
		})
	})
	function delete_evaluation_report($id){
		start_load()
		$.ajax({
			url:'ajax.php?action=delete_evaluation_report',
			method:'POST',
			data:{id:$id},
			success:function(resp){
				if(resp==1){
					alert_toast("Data successfully deleted",'success')
					setTimeout(function(){
						location.reload()
					},1500)
				}
			}
		})
	}
</script>